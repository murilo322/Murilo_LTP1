largura_requerida = float(input("Digite por favor a largura do retângulo: "))
altura_desejada = float(input("Digite a altura do retângulo, por favor: "))

perimetro = 2 * (largura_requerida + altura_desejada)
area = largura_requerida * altura_desejada

print(f"O perímetro do retângulo é {perimetro} unidades.")
print(f"A área do retângulo é {area} unidades quadradas.")
